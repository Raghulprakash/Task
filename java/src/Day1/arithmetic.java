package Day1;

public class arithmetic {

	public static void main(String[] args) {
		int a = 5;
		double b = 5.5;
		//addition
		System.out.println( a + "+" + b + " = " + (a+b));
		//subraction
		System.out.println( a + "-" + b + " = " + (a-b));
		//multiplication
		System.out.println( a + "*" + b + " = " + (a*b));
		//divison
		System.out.println( a + "/" + b + " = " + (a/b));
		//modulo
		System.out.println( a + "%" + b + " = " + (a%b));
	}

}
